namespace PAC.Geometry
{
    /// <summary>
    /// An angle that's a multiple of 90 degrees.
    /// </summary>
    /// <remarks>
    /// This should be interpreted as being clockwise.
    /// </remarks>
    public enum QuadrantalAngle
    {
        _0 = 0,
        _90 = 90,
        Minus90 = -90,
        _180 = 180
    }
}