namespace PAC
{
    /// <summary>
    /// This should be interpreted as being clockwise.
    /// </summary>
    public enum RotationAngle
    {
        _0 = 0,
        _90 = 90,
        Minus90 = -90,
        _180 = 180
    }

    public enum FlipDirection
    {
        None = 0,
        X = 1,
        Y = 2
    }
}